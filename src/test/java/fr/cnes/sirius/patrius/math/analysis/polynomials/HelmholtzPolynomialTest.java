    /**
     * Test computation of phr, dpphr, dsphr at a specific point
     */
    @Test
    public void testAtPoint() {

        // the ref data is generated with zu = .275;
        final double cd = .275;
        final double z = MathLib.sqrt(cd * cd / (1 - cd * cd));
        final Vector3D vect = new Vector3D(1., 0, z);

        Assert.assertEquals(cd, z / vect.getNorm(), Precision.EPSILON);

        final HelmholtzPolynomial poly = new HelmholtzPolynomial(40, 40);
        poly.computeHelmholtzPolynomial(vect);

        final double[][] ph = poly.getPh();
        final double[][] dpph = poly.getDpph();
        final double[][] dsph = poly.getDsph();

        int l, m;
        double phr, dpphr, dsphr;
        for (final double[] element : this.data) {
            l = (int) element[0];
            m = (int) element[1];

            phr = element[2];
            dpphr = element[3];
            dsphr = element[4];

            // eps * 4 because computed by another software
            if (!Precision.equals(0, phr, 1)) {
                Assert.assertEquals(0, (phr - ph[l][m]) / phr, Precision.DOUBLE_COMPARISON_EPSILON * 4);
            } else {
                Assert.assertEquals(phr, ph[l][m], Precision.DOUBLE_COMPARISON_EPSILON * 1);
            }

            if (!Precision.equals(0, dpphr, 1)) {
                Assert.assertEquals(0, (dpphr - dpph[l][m]) / dpphr, Precision.DOUBLE_COMPARISON_EPSILON * 1);
            } else {
                Assert.assertEquals(dpphr, dpph[l][m], Precision.DOUBLE_COMPARISON_EPSILON * 1);
            }

            if (!Precision.equals(0, dsphr, 1)) {
                Assert.assertEquals(0, (dsphr - dsph[l][m]) / dsphr, Precision.DOUBLE_COMPARISON_EPSILON * 1);
            } else {
                Assert.assertEquals(dsphr, dsph[l][m], Precision.DOUBLE_COMPARISON_EPSILON * 1);
            }

        }
    }

