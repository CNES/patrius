    /**
     * Test computation of constant coefficients
     */
    @Test
    public void testComputeConstantCoefficients() {

        final int order = 40;
        final int degree = 40;

        final double[] refHmm = new double[order + 1];

        refHmm[0] = 1;
        refHmm[1] = MathLib.sqrt(3);
        for (int m = 2; m < refHmm.length; m++) {
            refHmm[m] = MathLib.sqrt(1 + 1. / (2 * m)) * refHmm[m - 1];
        }

        final double[] refdHmp1m = new double[order];
        refdHmp1m[0] = MathLib.sqrt(3);
        for (int m = 1; m < refdHmp1m.length; m++) {
            refdHmp1m[m] = MathLib.sqrt(2 * m + 3) * refHmm[m];
        }

        HelmholtzPolynomial val = new HelmholtzPolynomial(order, degree);

        double[][] ph = val.getPh();
        double[][] dpph = val.getDpph();
        double[][] dsph = val.getDsph();

        Assert.assertEquals(41, ph.length);
        Assert.assertEquals(41, ph[0].length);

        Assert.assertEquals(41, dpph.length);
        Assert.assertEquals(41, dpph[0].length);

        Assert.assertEquals(41, dsph.length);
        Assert.assertEquals(41, dsph[0].length);

        // test Hmm for m >= 0
        for (int i = 0; i < refHmm.length; i++) {
            Assert.assertEquals(0, (refHmm[i] - ph[i][i]) / refHmm[i], Precision.DOUBLE_COMPARISON_EPSILON);
        }

        // test H'_m+1_m for m >= 0
        for (int i = 0; i < refdHmp1m.length; i++) {
            Assert.assertEquals(0, (refdHmp1m[i] - dpph[i + 1][i]) / refdHmp1m[i],
                Precision.DOUBLE_COMPARISON_EPSILON);
        }

        val = new HelmholtzPolynomial(order, 30);

        ph = val.getPh();
        dpph = val.getDpph();
        dsph = val.getDsph();

        Assert.assertEquals(41, ph.length);
        Assert.assertEquals(31, ph[0].length);

        Assert.assertEquals(41, dpph.length);
        Assert.assertEquals(31, dpph[0].length);

    }

